    @Test
    @Transactional
    void createAuthor() throws Exception {
        int databaseSizeBeforeCreate = authorRepository.findAll().size();
        // Create the Author
        AuthorDTO authorDTO = authorMapper.toDto(author);
        restAuthorMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(authorDTO)))
            .andExpect(status().isCreated());

        // Validate the Author in the database
        List<Author> authorList = authorRepository.findAll();
        assertThat(authorList).hasSize(databaseSizeBeforeCreate + 1);
        Author testAuthor = authorList.get(authorList.size() - 1);
        assertThat(testAuthor.getFullname()).isEqualTo(DEFAULT_FULLNAME);
        assertThat(testAuthor.getBirthyear()).isEqualTo(DEFAULT_BIRTHYEAR);
        assertThat(testAuthor.getDeathyear()).isEqualTo(DEFAULT_DEATHYEAR);
        assertThat(testAuthor.getNote()).isEqualTo(DEFAULT_NOTE);
    }

